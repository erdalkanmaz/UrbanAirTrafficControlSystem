package com.airtraffic.rules;

/**
 * Trafik kuralı tipi enum
 */
public enum RuleType {
    SPEED_LIMIT,        // Hız limiti
    ENTRY_EXIT,         // Giriş/çıkış kuralları
    ALTITUDE,           // Yükseklik kuralları
    SEPARATION,         // Ayrım kuralları (araçlar arası mesafe)
    PRIORITY,           // Öncelik kuralları
    WEATHER,            // Hava durumu kuralları
    TIME_BASED,         // Zaman bazlı kurallar
    ZONE_ACCESS         // Bölge erişim kuralları
}











